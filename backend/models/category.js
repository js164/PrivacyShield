const mongoose = require('mongoose');

const CategorySchema = new mongoose.Schema({
  code: {
    type: String,
    required: true,
    unique: true
  },
  name: {
    type: String,
    required: true
  },
  positive_suggestion: {
    type: String,
    required: true
  },
  negative_suggestion: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('categorySchema', CategorySchema);
